    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try{
            String username = txtUsername.getText();
            String password = String.valueOf(txtPassword.getPassword());
            
            int flag = 0;
  
            ResultSet  resultset = db.load("SELECT * FROM admin",null);
            while(resultset.next()){
                String dbAdmin = resultset.getString("adminname");
                String dbPassword = resultset.getString("password");
                if(username.equals(dbAdmin)&&password.equals(dbPassword)){
                    
                    new MainForm().setVisible(true);
                    this.dispose();
                    flag=1;
                }
            }

            if(flag ==0){
                JOptionPane.showInternalMessageDialog(null, "Password atau username salah");

            }

                     

        }
        catch(NullPointerException ex){
            JOptionPane.showInternalMessageDialog(null, "Ada data yang masih kosong");

        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnLoginActionPerformed

